package coffee_machine_oop2;

public class CoffeeVO {
	
	String name;
	int price,change;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getChange() {
		return change;
	}
	public void setChange(int change) {
		this.change = change;
	}

	
}
