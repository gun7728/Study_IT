/**
 * 비교(관계)연산자 : <,>,<=,>=,==,!=    값을 비교, 값은 boolean 으로 나옴(true/false)
 */
package java_0810_2day;

public class java_09_비교연산자 {

	public static void main(String[] args) {
		int x=100,y=100;
		System.out.println("< : "+(x<y));
		System.out.println("> : "+(x>y));
		System.out.println("<= : "+(x<=y));
		System.out.println(">= : "+(x>=y));
		System.out.println("== : "+(x==y));
		System.out.println("!= : "+(x!=y));

	}//main

}//class
