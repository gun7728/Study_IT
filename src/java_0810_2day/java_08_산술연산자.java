package java_0810_2day;

public class java_08_산술연산자 {

	public static void main(String[] args) {
		// 산술연산자 : +,-,*,/,%(모듈러연산자=나머지연산자)
		int x = 100,y=100;
		System.out.println("+ : "+(x+y));
		System.out.println("- : "+(x-y));
		System.out.println("* : "+(x*y));
		System.out.println("/ : "+(x/y));
		System.out.println("% : "+(x%y));	// 짝/홀, 0/1 같은거 구분

	}//main

}//class
